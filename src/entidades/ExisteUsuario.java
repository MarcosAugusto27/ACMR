package entidades;

public class ExisteUsuario {
	private String email;
	private String senha;
	
	
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String nome) {
		this.email = nome;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}

}
